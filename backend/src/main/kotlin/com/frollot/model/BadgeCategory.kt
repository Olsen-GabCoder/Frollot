package com.frollot.model

/**
 * Énumération des catégories de badges.
 * Phase E.3 - Badges et Certifications
 */
enum class BadgeCategory {
    /**
     * Certifications professionnelles (ex: "Formation L'Oréal", "Expert Coloriste")
     */
    CERTIFICATION,

    /**
     * Compétitions et concours (ex: "Champion Régional 2023")
     */
    COMPETITION,

    /**
     * Formations continues (ex: "Formation Avancée Coloration")
     */
    FORMATION,

    /**
     * Partenariats et reconnaissances (ex: "Top 10 Coiffeurs", "Salon de l'Année")
     */
    PARTENARIAT
}

